/**
 * Enum representing the categories in a Yatzy game.
 * Each category corresponds to a scoring option in the game.
 */
public enum Category {
    CHANCE,
    YATZY,
    ONES,
    TWOS,
    THREES,
    FOURS,
    FIVES,
    SIXES,
    PAIR,
    TWO_PAIR,
    THREE_OF_A_KIND,
    FOUR_OF_A_KIND,
    SMALL_STRAIGHT,
    LARGE_STRAIGHT,
    FULL_HOUSE
}
